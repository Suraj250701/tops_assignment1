# This program demonstrates the creation of variables and different data types in Python.

# Define variables of different data types
integer_var = 42  # Integer
float_var = 3.14  # Float
complex_var = 1 + 2j  # Complex number
string_var = "Hello, World!"  # String
boolean_var = True  # Boolean
list_var = [1, 2, 3, 4, 5]  # List
tuple_var = (1, 2, 3)  # Tuple
dictionary_var = {"key1": "value1", "key2": "value2"}  # Dictionary
set_var = {1, 2, 3}  # Set
range_var = range(5)  # Range (not used in display but defined)
# Display the variables and their types
print("Integer Variable:", integer_var, "Type:", type(integer_var))
print("Float Variable:", float_var, "Type:", type(float_var))
print("Complex Variable:", complex_var, "Type:", type(complex_var))
print("String Variable:", string_var, "Type:", type(string_var))
print("Boolean Variable:", boolean_var, "Type:", type(boolean_var))
print("List Variable:", list_var, "Type:", type(list_var))
print("Tuple Variable:", tuple_var, "Type:", type(tuple_var))
print("Dictionary Variable:", dictionary_var, "Type:", type(dictionary_var))
print("Set Variable:", set_var, "Type:", type(set_var))
print("Range Variable:", range_var, "Type:", type(range_var))