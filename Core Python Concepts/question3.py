# Practical Example 2: How to create variables in Python?

# Numbers
age = 25
price = 19.99
temperature = -5

# Text
name = "Alice"
city = 'New York'  # Single or double quotes both work

# Boolean values
is_student = True
is_employed = False


# Assign same value to multiple variables
x = y = z = 0

# Assign different values simultaneously
name, age, city = "Bob", 30, "Boston"

# Swap variables easily
a, b = 10, 20
a, b = b, a  # Now a=20, b=10


data = 42        # Integer
data = "Hello"   # Now it's a string  
data = [1, 2, 3] # Now it's a list


# List (ordered, changeable)
fruits = ["apple", "banana", "orange"]

# Dictionary (key-value pairs)
person = {"name": "Alice", "age": 25}

# Tuple (ordered, unchangeable)
coordinates = (10, 20)


 